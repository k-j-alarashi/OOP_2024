package lab1;

public class Arithmatic_Operations {
    public static void main(String[] args) {
        int x = 5 , y = 3 ;
        System.out.println(x+" + "+y+" = "+(x+y));
        System.out.println(x+" - "+y+" = "+(x-y));
        System.out.println(x+" * "+y+" = "+(x*y));
        System.out.println(x+" / "+y+" = "+(x/y));
    }
}
